import numpy as np

v1 = np.array([2, 3])
v2 = np.array([3, 5])
v3 = np.array([1, 4])

# Soma de vetores, basta somar posição com posição
soma = v1 + v3
print("\n Soma de v1 e v3:", soma)

# Subtração de vetores, basta subtrair posição com posição
subtracao = v1 - v3
print("\n Subtração de v1 e v3:", subtracao)